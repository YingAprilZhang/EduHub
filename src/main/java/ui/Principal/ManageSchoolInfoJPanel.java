    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        school.setName(nameField.getText());
        Country country = business.getCountryDirectory().getCountryByName(countryField.getText());
        school.setCountry(country);
        JOptionPane.showMessageDialog(null, "School info saved successfully", "Info", JOptionPane.INFORMATION_MESSAGE);
        nameField.setText("");
        countryField.setText("");

        workArea.remove(this);
        CardLayout layout = (CardLayout) workArea.getLayout();
        layout.previous(workArea);
        
    }//GEN-LAST:event_btnSaveActionPerformed

